def add_device_subscription(
    device_id: str,
    resort_name: str,
    target_date: str,
    unsubscribe_token: str,
    notification_type: str = "push",
) -> None:
    """
    Add a subscription for a device.

    Args:
        device_id: Device identifier
        resort_name: Name of the resort
        target_date: Date to track (YYYY-MM-DD format)
        unsubscribe_token: Token for unsubscribing from this specific alert
        notification_type: Type of notification ('push', 'sms', 'both')
    """
    table = get_dynamodb_table()

    table.put_item(
        Item={
            "pk": f"DATE#{target_date}",
            "sk": f"RESORT#{resort_name}#DEVICE#{device_id}",
            "device_id": device_id,
            "resort_name": resort_name,
            "date": target_date,
            "unsubscribe_token": unsubscribe_token,
            "notification_type": notification_type,
            "last_status": "unknown",
            "created_at": datetime.utcnow().isoformat() + "Z",
        }
    )


def get_device_subscription(
    device_id: str, resort_name: str, target_date: str
) -> dict[str, Any] | None:
    """
    Get a specific subscription for a device.

    Returns:
        Subscription record or None if not found
    """
    table = get_dynamodb_table()

    response = table.get_item(
        Key={
            "pk": f"DATE#{target_date}",
            "sk": f"RESORT#{resort_name}#DEVICE#{device_id}",
        }
    )

    return response.get("Item")


def remove_device_subscription(
    device_id: str, resort_name: str, target_date: str
) -> bool:
    """
    Remove a subscription for a device.

    Returns:
        True if deleted, False if not found
    """
    table = get_dynamodb_table()

    response = table.delete_item(
        Key={
            "pk": f"DATE#{target_date}",
            "sk": f"RESORT#{resort_name}#DEVICE#{device_id}",
        },
        ReturnValues="ALL_OLD",
    )

    return "Attributes" in response


def get_subscriptions_by_device(device_id: str) -> list[dict[str, Any]]:
    """
    Get all subscriptions for a device.

    Returns:
        List of subscription records
    """
    from boto3.dynamodb.conditions import Attr

    table = get_dynamodb_table()
    today = datetime.now().strftime("%Y-%m-%d")

    # Scan for device subscriptions with dates >= today
    response = table.scan(
        FilterExpression=Attr("date").gte(today)
        & Attr("sk").begins_with("RESORT#")
        & Attr("device_id").eq(device_id)
    )

    return response.get("Items", [])


def remove_all_device_subscriptions(device_id: str) -> int:
    """
    Remove all subscriptions for a device.

    Returns:
        Number of subscriptions removed
    """
    subscriptions = get_subscriptions_by_device(device_id)
    table = get_dynamodb_table()

    for sub in subscriptions:
        table.delete_item(Key={"pk": sub["pk"], "sk": sub["sk"]})

    return len(subscriptions)
